export function translateDisease(lang: Lang, disease: string): string {
  if (lang === "en" || !disease) return disease;
  const trimmed = disease.trim();
  const map = DISEASE_TRANSLATIONS[trimmed];
  if (!map) return disease;
  return lang === "te" ? map.te : map.ur;
}

